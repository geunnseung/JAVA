
public class Day07_Array {

	public static void main(String[] args) {
		
		
		int[] arr1 = {1, 2, 3, 4, 5};
		int[] arr2 = {5, 6, 7, 8, 9};

		for(int i=0; i<arr1.length; i++) {
		    System.out.println(arr1[i]);
		}
		for(int j=0; j<arr2.length; j++) {
			System.out.println(arr2[j]);
		}
	}
}
