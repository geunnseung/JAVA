
public class Ch01_Print {

	public static void main(String[] args) {
	
		System.out.println("Hello");
		System.out.println("Hello");
		System.out.println("Hello");
		//println은 줄바꿈
		System.out.print("Hello");
		System.out.print("Hello");
		System.out.print("Hello\n");
		//print는 줄바꿈 xx
		//print() - 출력후에 줄바꿈을 안함
		//println() - 출력 후에 줄바꿈을 함
		System.out.print("3+5\n");
		System.out.println(3+5);
		System.out.println(3*5);
		System.out.println(3-5);
		System.out.println(3/5);
		

		
	}

}
